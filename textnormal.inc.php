	public static function lower($str) {
		// UTF sequences starting c2-c9 can be folded
		$tmp = strtr($str, "\xc2\xc3\xc4\xc5\xc6\xc7\xc8\xc9", "\xf8\xf8\xf9\xf9\xf9\xf9\xf9\xf9");

		if ($tmp !== $str) {
			// if there are no c4-c9 then we can use a shorter, faster table
			if (strpos($tmp, "\xf9") === false)
				$str = strtr($str, self::$shortlowertable);
			else
				$str = strtr($str, self::$lowercharstable);
		}
	}

	public static function spaces($str) {

		// space
		$str = str_replace("\xc2\xa0", ' ', $str);
		$str = strtr($str, "\r\n\t\v\f", '     ');

		while ($str !== ($tmp = str_replace('  ', ' ', $str)))
			$str = $tmp;

		return trim($str);
	}

	public static function chars($str) {

		// UTF sequences starting c2-c9 can be folded, cc-cd can be combining marks
		$tmp = strtr($str, "\xc2\xc3\xc4\xc5\xc6\xc7\xc8\xc9\xcc\xcd", "\xf8\xf8\xf9\xf9\xf9\xf9\xf9\xf9\xfa\xfa");

		if ($tmp !== $str) {
			// if there are no c4-c9 then we can use a shorter, faster table
			if (strpos($tmp, "\xf9") === false)
				$str = strtr($str, self::$shortnormaltable);
			else
				$str = strtr($str, self::$normalletterstable);

			if (strpos($tmp, "\xfa") !== false)
				$str = strtr($str, self::$combiningmarkstable);
		}

		return strtr($str, 'ABCDEFGHIJKLMNOPQRSTUVWXYZ', 'abcdefghijklmnopqrstuvwxyz');
	}

	public static function asciify($str) {
		// converts everything down to:
		// - lowercase letters (a to z)
		// - digits (0 to 9)
		// - apostrophe ('), period (.), hyphen/minus (-) and underscore 
		//   when they occur in the middle of a word or number
		// - space (multiple spaces become one)
		// This converts unicode letters up to x24f to lowercase ascii
		// and converts some other unicode forms of hyphen and apostrophe
		
		$str = self::chars($str);

		// preserved chars
		// . and - need to proceed a number or . OR come between two alphanums
		// all others (eg ' and _) need to come between two alphanums
		
		// these two operations separately were faster than combining them
		$str = preg_replace('/
			[.\'_](?![a-z\d])
			| (?<![a-z\d])[\'_]+
			| (?<![a-z\d])\.(?!\d)
			| (?<![a-z\d])-(?![.\d])
			| (?<=[a-z\d])-(?![a-z\d])
			| ,(?!\d)
			| (?<!\d),
			/Sx', ' ', $str);
		$str = preg_replace('/[^a-z\d.\'\-_,]+/S', ' ', $str);

		return trim($str);
	}
	
	public function naturalsortindex($normaliseletters = true)
	{
		$webchars = $this->webchars;
		$this->webchars = false;
		$str = $this->normal($normaliseletters);
		$this->webchars = $webchars;
		
		$str = preg_replace('/^the |^an? /i', '', $str);
		
		//if (preg_match('/[0-9]/', $str))
		$str = preg_replace_callback('/(?<![0-9]\.)[1-9][0-9]*+/S', array($this, 'numbertrans_callback'), $str);
		
		return $str;
	}
	
	public function numbertrans_callback($matches)
		//todo does this have to be public?
	{
		$len = strlen($matches[0]);
		if ($len <= 9) return (string)($len - 1) . $matches[0];
		return '9' . (string)($len - 10) . $matches[0];
	}

